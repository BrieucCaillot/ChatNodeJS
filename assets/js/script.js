jQuery(document).ready(function($) {

	$("#skyblue").click(function(){
		$("body").css("background-color", "skyblue");
	});

	$("#blue").click(function(){
		$("body").css("background-color", "blue");
	});

	$("#emo").click(function(){
		$("body").css("background-color", "#000");
	});

	$("#red").click(function(){
		$("body").css("background-color", "red");
	});
});
